def flatten_and_unique(nested_lists):
    # Создаем множество для хранения уникальных строк
    unique_strings = set()

    # Проходим по каждому подсписку и добавляем строки во множество
    for sublist in nested_lists:
        for item in sublist:
            unique_strings.add(item)

    # Преобразуем множество обратно в список и сортируем его
    sorted_unique_strings = sorted(unique_strings)

    return sorted_unique_strings


